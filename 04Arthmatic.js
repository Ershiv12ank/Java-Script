/*
     Arthmatic operator
     +->Add
     - -> subtract
     * -> Multiply
     / -> Divde

     % -> Modlus
     ** -> Exponentiation or power
     ++ -> increment  (z++=z+1)
     -- -> Decrement 
*/

let a=22;
let b=10;
let c=2;
let d=3;

console.log("a =",a,"b=",b);
console.log("a + b = ",a+b);
console.log("a - b = ",a-b);
console.log("a * b = ",a*b);
console.log("a / b = ",a/b);
console.log("a % b = ",a%b);
console.log("a ** b = ",c**d);
console.log("a % b = ",a++);
console.log("a % b = ",a--);
console.log("a % b = ",--a);
console.log("a % b = ",++a);


